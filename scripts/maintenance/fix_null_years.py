# ── Constants ──────────────────────────────────────────────────────────────

OPERATIONS_BATCH_SIZE = 200
DEFAULT_SCROLL_BATCH_SIZE = 500
MAX_RETRIES = 5
BASE_BACKOFF = 2.0
LLM_BATCH_SIZE = 25
