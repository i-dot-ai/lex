#!/usr/bin/env python
"""
Recover legislation_year for sections where deterministic parsing returned null.

These are pre-1963 legislation with nonstandard regnal year URIs generated by
LLM-based OCR — hallucinations, inconsistent formatting, freetext monarch names.

TWO-TIER APPROACH:
  Tier 1 (deterministic): regnal.parse_legislation_year() — 10 strategies covering
    standard URIs, regnal years with nonstandard separators, freetext citations,
    combined reign references, embedded years, and short title text extraction.
    See src/lex/legislation/regnal.py for the full strategy list.

  Tier 2 (LLM): Azure OpenAI gpt-5-nano with monarch table in system prompt.
    Batches 25 URIs per call, medium reasoning effort. Writes to Qdrant
    continuously as batches complete. Only for genuinely ambiguous records.

Results (March 2026):
  Original null-year: 213,356 sections
  Tier 1 (deterministic): ~204,400 recovered
  Tier 2 (LLM):           ~4,889 recovered
  Remaining null:          ~3,996 (genuinely unrecoverable)
  Final coverage:          ~99.81%

Usage:
    uv run python scripts/maintenance/fix_null_years.py                        # dry run
    uv run python scripts/maintenance/fix_null_years.py --apply                # apply all
    uv run python scripts/maintenance/fix_null_years.py --apply --tier1-only   # deterministic only
    uv run python scripts/maintenance/fix_null_years.py --apply --tier2-only   # LLM only
"""

